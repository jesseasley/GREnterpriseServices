<span class="caption">Resumen</span>
<br></br>
Coagulaci&oacute;n es la operaci&oacute;n fisicoqu&iacute;mica  m&aacute;s importante empleada en el tratamiento del agua. Este proceso es usado 
para causar la desestabilizaci&oacute;n y agregaci&oacute;n de las peque&ntilde;as part&iacute;culas y convertirlas  a m&aacute;s tama&ntilde;o. Contaminantes 
del agua tales como iones (metales pesados) y coloides (org&aacute;nicos e inorg&aacute;nicos) son contenidos ante todo en 
soluciones debido a cargas el&eacute;ctricas.
<br></br>

Schulze en 1882, demostr&oacute; que sistemas coloidales pueden ser desestabilizados agregando iones que tengan una carga 
opuesta a la del coloide (Benfield et al., 1982). Los coloides desestabilizados pueden ser agregados y 
subsecuentemente  eliminados por sedimentaci&oacute;n y/o filtraci&oacute;n.
<br></br>
La coagulaci&oacute;n puede ser lograda por m&eacute;todos qu&iacute;micos o el&eacute;ctricos. La coagulaci&oacute;n qu&iacute;mica en estos tiempos esta 
cada d&iacute;a menos aceptada por los altos costos asociados con los tratamientos qu&iacute;micos (el gran volumen de lodo 
generado, as&iacute; como la categorizaci&oacute;n de los residuos peligrosos de hidr&oacute;xidos de metales, y el alto costo de 
los productos qu&iacute;micos que se requiere para lograr la coagulaci&oacute;n).
<br></br>
La Electrocoagulaci&oacute;n (EC) que es el paso de corriente el&eacute;ctrica a trav&eacute;s del agua, ha demostrado ser muy 
efectiva en la eliminaci&oacute;n de varios contaminantes en el agua. Sistemas de electrocoagulaci&oacute;n existen por 
mucho tiempo (Dieterich, patente 1906), usando una variedad de geometr&iacute;as de anodo y c&aacute;todo, incluyendo 
