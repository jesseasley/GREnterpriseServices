<br></br>
El proceso de electrocoagulaci&oacute;n est&aacute; basado en un principio cient&iacute;fico comprobado que incluye las reacciones de los 
contaminantes del agua con el paso de intensos campos el&eacute;ctricos as&iacute; como la inducci&oacute;n de oxidaci&oacute;n  el&eacute;ctricamente y 
reacciones de reducci&oacute;n. Este proceso es capaz de eliminar en m&aacute;s del 99% de cationes  en algunos metales pesados y es 
