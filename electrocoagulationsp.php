<span class="caption">Como Funciona</span>
<br></br>
El proceso de electrocoagulaci&oacute;n desestabiliza contaminantes suspendidos, emulsionados o disueltos  en un medio 
sedimentario por intermedio de la introducci&oacute;n de la corriente el&eacute;ctrica al medio. La corriente el&eacute;ctrica provee la 
fuerza electromotriz que impulsa las reacciones qu&iacute;micas. Cuando estas reacciones son impulsadas o forzadas, los 
elementos o compuestos se acercan al estado m&aacute;s estable. Generalmente, este estado de estabilidad produce un s&oacute;lido 
un poco menos coloidal o menos emulsionado(o soluble) que el compuesto  en los valores de equilibrio. Cuando esto 
est&aacute; ocurriendo los contaminantes forman entidades hidrofobicas precipitadas y pueden ser f&aacute;cilmente eliminadas a 
trav&eacute;s de varias t&eacute;cnicas secundarias de separaci&oacute;n. 
<br></br>
Electrocoagulaci&oacute;n utiliza la corriente directa (DC) lo cual causa el sacrificio de electrodos y de iones a eliminar 
y de contaminantes indeseables  por reacci&oacute;n qu&iacute;mica y precipitaci&oacute;n, o causando  que los materiales coloidales se 
juntan y despu&eacute;s son eliminados por flotaci&oacute;n electrol&iacute;tica. El sistema electroqu&iacute;mico ha probado ser viable en el 
proceso de una vasta variedad de aguas contaminadas. Estas aguas pueden ser de procedencia de residuos de pasta de 
papel, mineras, curtiembres, fabricas envasadoras, efluentes de aserraderos, camales, efluentes cargados con 
mercurio, plomo, as&iacute; como aguas servidas. Estas aguas son reducidas y convertidas en una agua clara, limpia, sin 
olor que puede volver a ser reutilizada. En la mayor&iacute;a de los casos, especialmente en aguas servidas el agua 
despu&eacute;s del tratamiento es mejor que el agua de origen.  
